package com.nextgenactionscript.vscode.asconfig;

import java.util.ArrayList;

public class ConfigVO {

	private String config;
	private boolean debug;
	private ArrayList<String> compilefiles;
	private String flexsdk;
	
	public String getConfig() {
		return config;
	}
	public void setConfig(String config) {
		this.config = config;
	}
	public boolean isDebug() {
		return debug;
	}
	public void setDebug(boolean debug) {
		this.debug = debug;
	}
	public ArrayList<String> getCompilefiles() {
		return compilefiles;
	}
	public void setCompilefiles(ArrayList<String> compilefiles) {
		this.compilefiles = compilefiles;
	}
	public String getFlexsdk() {
		return flexsdk;
	}
	public void setFlexsdk(String flexsdk) {
		this.flexsdk = flexsdk;
	}
}
